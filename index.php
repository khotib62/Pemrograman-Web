                    <p>Perusahaan Sony (ソニー株式会社 Sonī Kabushiki Kaisha, /ˈsoʊni/ soh-nee, disingkat SONY) adalah perusahaan konglomerat Jepang yang berkantor pusat di Minato, Tokyo.[7][1] Bisnisnya yang beragam meliputi elektronik konsumen dan profesional, permainan, hiburan, dan layanan keuangan.[8] Perusahaan ini memiliki bisnis hiburan musik terbesar di dunia,[9] bisnis konsol video game terbesar dan salah satu bisnis penerbitan video game terbesar, dan merupakan salah satu produsen produk elektronik terkemuka untuk pasar konsumen dan profesional, dan pemain terkemuka dalam industri hiburan film dan televisi.[10] [sumber yang lebih baik diperlukan] Sony berada di peringkat ke-97 dalam daftar 2018 Fortune Global 500.[11]
                        Sony Corporation adalah unit bisnis elektronik dan perusahaan induk dari Sony Group (ソニー・グループ Soni Gurupu), yang bergerak dalam bisnis melalui empat komponen operasinya: elektronik (AV, IT & produk komunikasi, semikonduktor, video game, layanan jaringan dan bisnis medis), film (film dan acara TV), musik (label rekaman dan penerbitan musik) dan layanan keuangan (perbankan dan asuransi).[12][13][14] Ini menjadikan Sony salah satu perusahaan hiburan paling komprehensif di dunia. Grup ini terdiri dari Sony Corporation, Sony Pictures, Sony Mobile, Sony Interactive Entertainment, Sony Music, Sony/ATV Music Publishing, Sony Financial Holdings, dan lainnya <br>
                        Sony dimulai setelah Perang Dunia II. Pada tahun 1946, Masaru Ibuka memulai sebuah toko elektronik di sebuah gedung department store di Tokyo. Perusahaan mulai dengan modal ¥ 190.000[17] dan total delapan karyawan.[18] Pada tanggal 7 Mei 1946, Ibuka bergabung dengan Akio Morita untuk mendirikan sebuah perusahaan bernama Tokyo Tsushin Kogyo (東京通信工業 Tōkyō Tsūshin Kōgyō) (Tokyo Telecommunications Engineering Corporation artinya Perusahaan Teknik Telekomunikasi Tokyo).[19] Perusahaan membangun tape recorder pertama Jepang, yang disebut Type-G.[19][20] Pada tahun 1958, perusahaan mengubah namanya menjadi "Sony".[21]
